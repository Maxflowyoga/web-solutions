package com.xrpl.models;



public class Model_XRP_NFT {
	
	
	String account;
	String ledger_index;
	String limit;
	String marker;
	String ledger_hash;
	
	
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getLedger_index() {
		return ledger_index;
	}
	public void setLedger_index(String ledger_index) {
		this.ledger_index = ledger_index;
	}
	public String getLimit() {
		return limit;
	}
	public void setLimit(String limit) {
		this.limit = limit;
	}
	public String getMarker() {
		return marker;
	}
	public void setMarker(String marker) {
		this.marker = marker;
	}
	public String getLedger_hash() {
		return ledger_hash;
	}
	public void setLedger_hash(String ledger_hash) {
		this.ledger_hash = ledger_hash;
	}
	
	
}
